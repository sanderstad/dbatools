		[object[]]$ExcludeDatabase,
		[Alias("All")]
		[parameter(Position = 4, ParameterSetName = "DbBackup")]
		[parameter(Position = 4, ParameterSetName = "DbAttachDetach")]
		[switch]$AllDatabases,
		[parameter(Position = 5, Mandatory = $true, ParameterSetName = "DbAttachDetach")]
		[switch]$DetachAttach,
